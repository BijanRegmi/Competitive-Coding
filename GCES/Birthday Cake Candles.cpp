// =============[ Solution Begin ]=============
void solve() {
    int n;
    cin >> n;
    int max = 0, x;
    map<int, int> freq;
    for (int i = 0; i < n; i++) {
        cin >> x;
        if (x > max)
            max = x;
        if (freq.find(x) == freq.end())
            freq[x] = 0;
        freq[x]++;
    }
    cout << freq[max];
}
// =============[ Solution End ]=============

int main() {
    bool testcase = false;
    int t = 1;
    if (testcase)
        cin >> t;

    while (t--) {
        solve();
        cout << endl;
    }
}
